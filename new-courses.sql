CREATE DATABASE university_database;
USE university_database;
-- DROP DATABASE university_database;
CREATE TABLE Faculties (
    Faculty_ID   VARCHAR(50)  NOT NULL,
    Faculty_Name VARCHAR(255) NOT NULL,
    Address      VARCHAR(255) NOT NULL,
    Subtitle     VARCHAR(MAX),
    Description  VARCHAR(MAX),
    PRIMARY KEY (Faculty_ID)
);

CREATE TABLE Departments (
    Department_ID     VARCHAR(50)  NOT NULL,
    Faculty_ID        VARCHAR(50)  NOT NULL,
    Department_Name   VARCHAR(255) NOT NULL,
    Subtitle          VARCHAR(MAX),
    Description       VARCHAR(MAX),

    PRIMARY KEY (Department_ID),
    FOREIGN KEY (Faculty_ID) REFERENCES Faculties(Faculty_ID)
);

CREATE TABLE Courses (
    Course_ID            VARCHAR(50)  NOT NULL PRIMARY KEY,
    Course_Name          VARCHAR(255) NOT NULL,
    Credit_Hours         INT          NOT NULL,
    Dependency_Course_ID VARCHAR(50)          ,

    FOREIGN KEY (Dependency_Course_ID) REFERENCES  Courses(Course_ID)
);

CREATE TABLE Department_Courses (
  Department_ID  VARCHAR(50) NOT NULL,
  Course_ID      VARCHAR(50) NOT NULL,
  Level          INT         NOT NULL,
  Semester       INT         NOT NULL,

  PRIMARY KEY (Department_ID, Course_ID),
  FOREIGN KEY (Department_ID) REFERENCES Departments(Department_ID),
  FOREIGN KEY (Course_ID) REFERENCES Courses(Course_ID)
);

CREATE TABLE Parents (
    Parent_ID     VARCHAR(50)   NOT NULL PRIMARY KEY,
    Name          VARCHAR(255)  NOT NULL,
    Date_Of_Birth DATE          NOT NULL,
    National_ID   VARCHAR(20)   NOT NULL,
    Mobile_No     VARCHAR(55)   NOT NULL,
    Email         VARCHAR(200)  CHECK  (Email LIKE '%@%.%' ) NOT NULL,
    Address       VARCHAR(255),
    Gender        VARCHAR(1)    CHECK  (Gender IN ('m','f')) NOT NULL
);

CREATE TABLE Students (
    Student_ID         VARCHAR(50)  PRIMARY KEY,
    Name               VARCHAR(255) NOT NULL,
    Date_Of_Birth      DATE         NOT NULL,
    National_ID        VARCHAR(20)  NOT NULL,
    Mobile_No          VARCHAR(20)  NOT NULL,
    Extra_Mobile_No    VARCHAR(20),
    Email              VARCHAR(50) CHECK  (Email LIKE '%@%.%' ) NOT NULL UNIQUE KEY,
    Address            VARCHAR(255),
    Gender             VARCHAR(1)  CHECK  (Gender IN ('m','f')) NOT NULL,
    Parent_ID          VARCHAR(50)  NOT NULL,
    Department_ID      VARCHAR(50)  NOT NULL,
    Section_Number     INT          NOT NULL,
    Level              INT          NOT NULL,
    GPA                FLOAT,
    Year_Of_Enrollment INT          NOT NULL,

    FOREIGN KEY (Parent_ID)     REFERENCES Parents(Parent_ID),
    FOREIGN KEY (Department_ID) REFERENCES Departments(Department_ID)
);


CREATE TABLE Login (
  	Email VARCHAR(200) CHECK (Email LIKE '%@%.%' )  PRIMARY KEY,
  	Password VARCHAR(50) NOT NULL,

    FOREIGN KEY (Email) REFERENCES Students(Email)
);


CREATE TABLE Student_Courses (
    Student_ID      VARCHAR(50)   NOT NULL,
    Course_ID       VARCHAR(50)   NOT NULL,
    Department_ID   VARCHAR(20)   NOT NULL,
    Section_Number  INT           NOT NULL,
 	Level           INT           NOT NULL,
    Year            INT           NOT NULL,
    Semester        INT           NOT NULL,
    Classwork_Grade FLOAT,
    Midterm_Grade   FLOAT,
    Finals_Grade     FLOAT,

    PRIMARY KEY (Student_ID, Course_ID,  Section_Number, Level, Year, Semester),
    FOREIGN KEY (Student_ID) REFERENCES  Students(Student_ID),
    FOREIGN KEY (Course_ID) REFERENCES  Courses(Course_ID),
    FOREIGN KEY (Department_ID) REFERENCES Departments(Department_ID)
);

CREATE TABLE Professors (
    Prof_ID       VARCHAR(50)   NOT NULL,
    Name          VARCHAR(255)  NOT NULL,
    Date_Of_Birth DATE          NOT NULL,
    National_ID   VARCHAR(20)   NOT NULL,
    Mobile_No     VARCHAR(55)   NOT NULL,
    Email         VARCHAR(200)  CHECK  (Email LIKE '%@%.%' ) NOT NULL,
    Address       VARCHAR(255)  NOT NULL,
    Gender        VARCHAR(1)    CHECK  (Gender IN ('m','f')) NOT NULL,
    Department_ID VARCHAR(20)   NOT NULL,
  
    PRIMARY KEY (Prof_ID),
    FOREIGN KEY (Department_ID) REFERENCES Departments(Department_ID)
);

CREATE TABLE Professor_Courses (
    Prof_ID         VARCHAR(50)    NOT NULL,
    Course_ID       VARCHAR(50)    NOT NULL,
    Department_ID   VARCHAR(20)    NOT NULL,
    Section_Number  INT            NOT NULL,
    Level           INT            NOT NULL, 
    Year            INT            NOT NULL,
    Semester        INT            NOT NULL,

    PRIMARY KEY (Prof_ID, Course_ID,  Department_ID, Section_Number, Level, Year, Semester),
    FOREIGN KEY (Prof_ID)    REFERENCES Professors(Prof_ID),
    FOREIGN KEY (Course_ID)  REFERENCES Courses(Course_ID),
    FOREIGN KEY (Department_ID) REFERENCES Departments(Department_ID)
);





